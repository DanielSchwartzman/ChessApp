package model.chess_pieces.AbstractClasses;

public abstract class SpecialChessPiece extends ChessPiece
{

    ////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////
    //Variables

    protected int timesMoved;

    //////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////




    ////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////
    //Constructor

    public SpecialChessPiece(int row,int col,int allegiance)
    {
        super(row,col,allegiance);
        timesMoved=0;
    }

    //////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////




    ////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////
    //Functions

    public void increaseTimesMoved() {timesMoved++;}
    public int getTimesMoved(){return timesMoved;}

    //////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////

}
